package com.astroganit.model;

public class BasicPlanetDataModel {
	String plaName;
	String sign;
	String degree;
	String naks;
	String plaCharan;

	public String getPlaName() {
		return plaName;
	}

	public void setPlaName(String plaName) {
		this.plaName = plaName;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getNaks() {
		return naks;
	}

	public void setNaks(String naks) {
		this.naks = naks;
	}

	public String getPlaCharan() {
		return plaCharan;
	}

	public void setPlaCharan(String plaCharan) {
		this.plaCharan = plaCharan;
	}

}
