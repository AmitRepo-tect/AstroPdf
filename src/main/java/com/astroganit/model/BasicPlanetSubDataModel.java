package com.astroganit.model;

public class BasicPlanetSubDataModel {
	String plaName;
	String plaDeg;
	String signLord;
	String nakshLord;
	String subLord;
	String subSubLord;

	public String getPlaName() {
		return plaName;
	}

	public void setPlaName(String plaName) {
		this.plaName = plaName;
	}

	public String getPlaDeg() {
		return plaDeg;
	}

	public void setPlaDeg(String plaDeg) {
		this.plaDeg = plaDeg;
	}

	public String getSignLord() {
		return signLord;
	}

	public void setSignLord(String signLord) {
		this.signLord = signLord;
	}

	public String getNakshLord() {
		return nakshLord;
	}

	public void setNakshLord(String nakshLord) {
		this.nakshLord = nakshLord;
	}

	public String getSubLord() {
		return subLord;
	}

	public void setSubLord(String subLord) {
		this.subLord = subLord;
	}

	public String getSubSubLord() {
		return subSubLord;
	}

	public void setSubSubLord(String subSubLord) {
		this.subSubLord = subSubLord;
	}

}
